local T = require("testing.testing")(describe, it)

T.Scenario(function(api)
  -- Load necessary plugins
  api:getPluginInstance(require('neodap.plugins.LaunchJsonSupport'))
  api:getPluginInstance(require('neodap.plugins.BreakpointApi'))
  api:getPluginInstance(require('neodap.plugins.ToggleBreakpoint'))
  api:getPluginInstance(require('neodap.plugins.Variables4'))

  -- Set up initial state and launch session
  T.cmd("edit lua/testing/fixtures/variables/complex.js")
  T.cmd("normal! 6j") -- Move to line with variables
  T.cmd("NeodapToggleBreakpoint")
  T.cmd("NeodapLaunchClosest Variables")
  T.sleep(1500) -- Wait for session and breakpoint hit

  -- Open the variables tree popup
  T.cmd("Variables4Tree")
  T.sleep(300)
  T.TerminalSnapshot('auto_drill_initial_full_tree')

  -- Navigate into the tree
  T.cmd("normal! l") -- Expand Local scope
  T.sleep(500)
  T.cmd("normal! j") -- Move down to a variable
  T.TerminalSnapshot('auto_drill_position_in_local_scope')

  -- Enable focus mode
  T.cmd("normal! f")
  T.sleep(200)
  T.TerminalSnapshot('auto_drill_focus_mode_on_local_scope')

  -- Navigate deeper - focus should auto-drill
  T.cmd("normal! j")
  T.cmd("normal! j") -- Move to expandable variable
  T.cmd("normal! l") -- Expand and enter variable
  T.sleep(500)
  T.TerminalSnapshot('auto_drill_focused_on_variable_parent')

  -- Continue navigating - focus should follow
  T.cmd("normal! j")
  T.cmd("normal! l") -- Go deeper if possible
  T.sleep(300)
  T.TerminalSnapshot('auto_drill_deeper_navigation')

  -- Navigate back up - focus should update
  T.cmd("normal! h") -- Move to parent
  T.sleep(200)
  T.TerminalSnapshot('auto_drill_moved_to_parent')

  -- Continue up - focus should keep updating
  T.cmd("normal! h") -- Move up again
  T.sleep(200)
  T.TerminalSnapshot('auto_drill_moved_up_further')

  -- Toggle focus mode off
  T.cmd("normal! f")
  T.sleep(200)
  T.TerminalSnapshot('auto_drill_focus_mode_off_full_tree')

  -- Navigate and toggle focus again
  T.cmd("normal! l") -- Enter a node
  T.sleep(300)
  T.cmd("normal! f") -- Toggle focus on
  T.sleep(200)
  T.TerminalSnapshot('auto_drill_focus_toggled_at_new_position')

  -- Verify j/k navigation also triggers auto-drill
  T.cmd("normal! k") -- Move up
  T.sleep(200)
  T.TerminalSnapshot('auto_drill_k_navigation_updates_focus')

  T.cmd("normal! j") -- Move down
  T.cmd("normal! j") -- Move down more
  T.sleep(200)
  T.TerminalSnapshot('auto_drill_j_navigation_updates_focus')

  -- Close popup
  T.cmd("normal! q")
  T.TerminalSnapshot('auto_drill_test_complete')
end)




